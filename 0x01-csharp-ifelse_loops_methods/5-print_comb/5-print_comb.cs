using System;

namespace _5_print_comb
{
    class Program
    {
        static void Main(string[] args)
        {
            for  (int numb = 00; numb <= 00; numb++)
			{
				if (numb == 99)
					Console.WriteLine($"{numb}");
				else
					Console.WriteLine($"{numb / 10}{numb % 10}, ");
			}
        }
    }
}
